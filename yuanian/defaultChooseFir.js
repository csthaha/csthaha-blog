/**
 * 默认选中数组列表的第一项（父项不可选中）
 * @param {Array} data
 * @returns {String} result
 */
function defaultChooseFirst(data) {
    if (!Array.isArray(data)) return;
    const copyData = [...data];
    let result = null;
    while (copyData.length) {
      const currItem = copyData.shift();
      const { isLeaf, children, objectId} = currItem;
      if (isLeaf) {
        result = objectId;
        break;
      } else {
        if (children && children.length) {
          copyData.unshift(...children);
        }
      }
    }
    return result;
  }
  

